public class Codes {
    private String routeName;
    private String routeCode;

    public Codes(String routeName, String routeCode) {
        this.routeName = routeName;
        this.routeCode = routeCode;
    }

    public String getRouteName() {
        return routeName;
    }

    public String getRouteCode() {
        return routeCode;
    }
}
